# print() is a built-in function used to display output on the console.
print("Hello, World!")

# Strings are enclosed in single (' ') or double (" ") quotes.
print('Welcome to Python')

# print() can also display numbers and expressions.
print(2026)
print(10 + 20)

# Multiple print() statements print on separate lines.
print("Line 1")
print("Line 2")