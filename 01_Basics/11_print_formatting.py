# ============================================
# Topic: Escape Sequences
# Folder: 01_Basics
# Description: Special character sequences used inside strings.
# ============================================

# --------------------------------------------
# New Line (\n)
# --------------------------------------------

print("Hello\nWorld")

# Output:
# Hello
# World

# --------------------------------------------
# Tab (\t)
# --------------------------------------------

print("Name\tAge")

# Output:
# Name    Age

# --------------------------------------------
# Backslash (\\)
# --------------------------------------------

print("C:\\Users\\Yash")

# --------------------------------------------
# Single Quote (\')
# --------------------------------------------

print('It\'s a beautiful day.')

# --------------------------------------------
# Double Quote (\")
# --------------------------------------------

print("She said, \"Hello!\"")

# --------------------------------------------
# Carriage Return (\r)
# --------------------------------------------

print("12345\rABC")

# Output may vary depending on the terminal.

# --------------------------------------------
# Backspace (\b)
# --------------------------------------------

print("Helloo\b")

# Output:
# Hello

# --------------------------------------------
# Common Mistakes
# --------------------------------------------

# Wrong
# print("C:\Users\Yash")

# Correct
print("C:\\Users\\Yash")

# --------------------------------------------
# Best Practices
# --------------------------------------------

# ✔ Use escape sequences only when needed.
# ✔ Use raw strings (r"...") for file paths and regular expressions.

# --------------------------------------------
# Quick Revision
# --------------------------------------------

# \n  -> New Line
# \t  -> Tab
# \\  -> Backslash
# \'  -> Single Quote
# \"  -> Double Quote
# \r  -> Carriage Return
# \b  -> Backspace

